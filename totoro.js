class Totoro{
  constructor(x, y, scale){
    this.x = x;
    this.y = y;
    this.scale = scale;
  }


  drawTotoroParts(totoroPart){
    beginShape();
    for(var a = 0; a < totoroPart.length; a++){
      curveVertex(this.x + totoroPart[a].x, this.y + totoroPart[a].y);
    }
   endShape(CLOSE);
  }

  drawTototoStomachShape(totoroPart, x, y){
    beginShape();
    for(var a = 0; a < totoroPart.length; a++){
      curveVertex(x + totoroPart[a].x, y + totoroPart[a].y);
    }
   endShape(CLOSE);
  }

  drawTotoroEye(){
    //totoro_left_eyes
    noStroke();
    fill(255);
    ellipse(this.x + 103,  this.y + 114, 23, 23);
    fill(0);
    ellipse(this.x + 103, this.y + 114, 13, 13);
    fill(255);
    ellipse(this.x + 100, this.y + 110, 3, 3);

    //totoro_right_eyes
    noStroke();
    fill(255);
    ellipse(this.x + 192, this.y + 114, 23, 23);
    fill(0);
    ellipse(this.x + 192, this.y + 114, 13, 13);
    fill(255);
    ellipse(this.x + 188, this.y + 110, 3, 3);
  }


  drawTotoro(){

      scale(this.scale);

      fill(CON.COLOR.totoroBody); // totoro body color
      strokeWeight(2);
      stroke(0);
      this.drawTotoroParts(totoro_body);
      this.drawTotoroParts(totoro_left_ear);
      this.drawTotoroParts(totoro_right_ear);
      noStroke();

      //line clear
      ellipse(this.x + 204, this.y + 365, 10, 10);
      ellipse(this.x + 90, this.y + 365, 10, 10);

      stroke(0);
      fill(CON.COLOR.totoroHeadLeaf);
      this.drawTotoroParts(totoro_head_leaf);
      stroke(CON.COLOR.totoroHeadLeaf);
      this.drawTotoroParts(totoro_leaf_head);

      stroke(0);
      fill(0);
      this.drawTotoroParts(totoro_left_Beard_01);
      this.drawTotoroParts(totoro_left_Beard_02);
      this.drawTotoroParts(totoro_left_Beard_03);

      this.drawTotoroParts(totoro_right_Beard_01);
      this.drawTotoroParts(totoro_right_Beard_02);
      this.drawTotoroParts(totoro_right_Beard_03);

      this.drawTotoroParts(totoro_nose);

      fill(CON.COLOR.totoroStomach); // totoro_stomach color
      this.drawTotoroParts(totoro_stomach);

      fill(CON.COLOR.totoroBody);
      noStroke();
      this.drawTototoStomachShape(totoro_stomach_shape, this.x, this.y);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x + 43, this.y + 13);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x - 43, this.y + 13);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x - 64, this.y + 39);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x - 21, this.y + 39);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x + 22, this.y + 39);
      this.drawTototoStomachShape(totoro_stomach_shape, this.x + 65, this.y + 39);

      this.drawTotoroEye();
  }
}
